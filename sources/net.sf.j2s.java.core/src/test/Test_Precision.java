	private static void test(double d, double value, int pow, int prec) {
		if (testCheck(d, value, pow, prec, 'f')) {
			testCheck(d, value, pow, prec, 'g');
			testCheck(d, value, pow, prec, 'e');
		}
	}
	
	private static boolean testCheck(double d, double value, int pow, int prec, char c) {
		String test = String.format("%1." + prec + c, value);
		String key = getKey(d, pow, prec);
		String val = getCheck(d, key, c);
		if (val == null) {
			System.out.println("Skipping " + key + " not found '"+c+"' JavaScript: " + test);
			return false;
		}
		if (!val.equals(test) && (c != 'f' || val.length() < 18))
			System.out.println("String.format difference '"+c+"' : " + key + "\n" + " in Java: " + val + " in JavaScript: " + test);
		return true;
	}

	private static String getKey(double d, int pow, int prec) {
		return d  + ",pow=" + pow + ",prec=" + prec + " ";
	}

	private static String getCheck(double d, String key, char c) {
		int ipt = stest.indexOf(key);
		if (ipt < 0) {
			return null;
		}
		
		String[] ref = stest.substring(ipt, stest.indexOf('\n', ipt)).split(" ");
		String val = null;
		switch(c) {
		case 'f':
			val = ref[1];
			break;
		case 'g':
			val = ref[2];
			break;
		case 'e':
			val = ref[3];
			break;
		}
		return val;
	}

	/**
	 * In Java, we just create the reference string:
	 * 
	 * @param d
	 */
	@SuppressWarnings("unused")
	private static void test2(int pow, double d) {
		double v =  d * Math.pow(10, pow);
		for (int prec = 0; prec < 12; prec++) {
			if (/**@j2sNative 1?true:*/false)
				test(d, v, pow, prec);
			else
				dumpJava(d, v, pow, prec);
		}
	}

